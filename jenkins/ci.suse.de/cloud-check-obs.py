    def rdiff(self, old_project, package, new_project):
        os.environ["PAGER"] = "cat -v"
        process = self.iosc.rdiff(
            old_project, package, new_project, _ok_code=[0, 1]
        )
        return process

    def ls_set(self, project):
        return {
            package.strip() for package in self.iosc.ls(project, _iter=True)
        }

    def logproc(self, process, title):
        result = True
        if process.stderr != b"":
            logging.error(
                "stderr from %s starts on next line\n%s",
                title,
                process.stderr.decode(),
            )
            logging.error("stderr from %s finished", title)
            result = False
        if process.stdout != b"":
            logging.info(
                "stdout from %s starts on next line\n%s",
                title,
                process.stdout.decode(),
            )
            logging.info("stdout from %s finished", title)
        if process.exit_code != 0:
            logging.error("exit code from %s is %s", title, process.exit_code)
            result = False
        return result

    def init_packages(self):
        self.project_product = self.project_product_map_version[self.version]
        self.project_product_update = self.project_product + ":Update"
        self.project_devel = "Devel:Cloud:" + str(self.version)
        self.project_devel_staging = self.project_devel + ":Staging"
        self.project_rubygem = "Devel:Cloud:Shared:Rubygem"
        self.project_obs = self.project_obs_map_version[self.version]
        self.project_obs_staging = self.project_obs + ":Staging"
        self.project_obs_venv = self.project_obs + ":venv"
        self.packages_product = self.ls_set(self.project_product)
        maintenance = re.compile(r"\.\d+$")
        self.packages_product_update = {
            p
            for p in self.ls_set(self.project_product_update)
            if not maintenance.search(p)
        }
        self.packages_devel = self.ls_set(self.project_devel)
        self.packages_devel_staging = self.ls_set(self.project_devel_staging)
        self.packages_rubygem = self.ls_set(self.project_rubygem)
        self.packages_obs = self.ls_set(self.project_obs)
        self.packages_obs_staging = self.ls_set(self.project_obs_staging)
        if self.version != 7:
            self.packages_obs_venv = self.ls_set(self.project_obs_venv)
        else:
            self.packages_obs_venv = set()

        logging.info(
            "init packages results in: %s", pprint.pformat(self.__dict__)
        )

        self.packages_product_full = self.packages_product.union(
            self.packages_product_update
        )
        return True

    def is_diff_noop(self, stdout):
        """
        Return True if a package diff only contains a few changes in the spec
        file that do not change the build output.

        These changes are created by the spec file formatter (package
        obs-service-format_spec_file) that is run from osc commit. This is
        called in the CI updating package code from upstream even when it would
        otherwise not change anything. osc commit --skip-local-service-run
        would avoid this, but would then also not do changes we would like it
        to do.
        """
        re_file_start = (
            r"Index: [^\n]+\.spec\n={67}\n"
            r"--- [^\n]+ \(revision \d+\)\n"
            r"\+\+\+ [^\n]+ \(revision \d+\)\n"
        )
        re_hunks = (
            r"(@@[-+, 0-9]+@@(\n [^\n]*|"
            r"\n-(Url|# Copyright|# Please submit bugfixes)[^\n]*|"
            r"\n+(URL|# Copyright|# Please submit bugfixes)[^\n]*)+\n)+"
        )
        re_noop_diff = (re_file_start + re_hunks).encode()
        matches = re.match(re_noop_diff, stdout)
        logging.info("is_diff_noop match: %s", matches)
        if matches is None:
            return False
        return True

    def is_diff_ok_change(self, stdout):
        """
        Return True if a package diff has only additions and no removals in its
        .changes files.
        """
        re_changes_file_only_has_additions = (
            r"("
            r"(Index: [^\n]+(?<!\.changes)\n={67}\n"
            r"(--- [^\n]+ \(revision \d+\)\n"
            r"\+\+\+ [^\n]+ \(revision \d+\)\n"
            r"(@@[-+, 0-9]+@@(\n [^\n]*|\n-[^\n]*|\n+[^\n]*)+\n)+|"
            r"Binary files [^\n]+ \(revision \d+\) and"
            r" [^\n]+ \(revision \d+\) differ\n))|"
            r"(Index: [^\n]+\.changes\n={67}\n"
            r"--- [^\n]+ \(revision \d+\)\n"
            r"\+\+\+ [^\n]+ \(revision \d+\)\n"
            r"(@@[-+, 0-9]+@@(\n [^\n]*|\n+[^\n]*)+\n)+)"
            r")+"
        ).encode()
        matches = re.match(re_changes_file_only_has_additions, stdout)
        logging.info("is_diff_ok_change 1st match: %s", matches)
        if matches is None:
            return False
        re_at_least_one_changes_file = (
            r"([^\n]*\n)*(Index: [^\n]+\.changes\n={67}\n)([^\n]*\n)*".encode()
        )
        matches = re.match(re_at_least_one_changes_file, stdout)
        logging.info("is_diff_ok_change 2nd match: %s", matches)
        if matches is None:
            return False
        return True

    def rdiff_packages_devel(self):
        """
        Diff each package and check if the differences are self.is_diff_noop()
        or self.is_diff_ok_change(). List the packages for each of these.

        Return True if there were no errors found with the diffs.
        """
        diff = set()
        diff_noop = set()
        diff_err = set()
        diff_missing = set()

        def check(missing=False):
            if (
                process.exit_code == 0
                and process.stdout == b""
                and process.stderr == b""
            ):
                pass
            else:
                if self.is_diff_noop(process.stdout):
                    diff_noop.add(p)
                else:
                    diff.add(p)
                    if missing:
                        diff_missing.add(p)
                    if not self.is_diff_ok_change(process.stdout):
                        logging.error(
                            "Diff of %s is not ok. It removes lines from a"
                            " .changes file or has changes in other files"
                            " without an addition to a .changes file or"
                            " osc rdiff failed.",
                            p,
                        )
                        diff_err.add(p)
                if not self.logproc(process, "rdiff on {0}".format(p)):
                    diff_err.add(p)

        for p in self.packages_devel.union(self.packages_rubygem):
            if p.startswith("_product:"):
                # skip sub-packages derived from _product which we already diff
                continue
            if p not in self.packages_product_full:
                logging.info("Skipping rdiff as it is not in product: %s", p)
                continue

            diff_new = self.project_devel
            if p not in self.packages_devel and p in self.packages_rubygem:
                diff_new = self.project_rubygem
            process = self.rdiff(self.project_product_update, p, diff_new)
            check()

            if p not in self.packages_obs and p not in self.packages_obs_venv:
                continue
            diff_new = self.project_obs
            if p in self.packages_obs_venv:
                diff_new = self.project_obs_venv
            elif p not in self.packages_obs:
                continue
            process = self.rdiff(self.project_product_update, p, diff_new)
            check(
                diff_new == self.project_obs
                and p not in self.project_devel_staging
            )

        for p in self.packages_obs.union(
            self.packages_obs_venv
        ) - self.packages_devel.union(self.packages_rubygem):
            if p not in self.packages_product_full:
                logging.info("Skipping rdiff as it is not in product: %s", p)
                continue
            diff_new = self.project_obs
            if p in self.packages_obs_venv:
                diff_new = self.project_obs_venv
            process = self.rdiff(self.project_product_update, p, diff_new)
            check(True)

        if diff:
            logging.warning(
                "Packages in %s or %s had differences" " to %s : %s",
                self.project_devel,
                self.project_rubygem,
                self.project_product_update,
                diff,
            )
        if diff_noop:
            logging.info(
                "Packages in %s or %s had differences"
                " to %s that were a noop: %s",
                self.project_devel,
                self.project_rubygem,
                self.project_product_update,
                diff_noop,
            )
        if diff_err:
            logging.error(
                "Packages in %s or %s had differences"
                " to %s and also errors: %s",
                self.project_devel,
                self.project_rubygem,
                self.project_product_update,
                diff_err,
            )
            return False
        return True

    def rdiff_packages_obs(self):
        diff = set()
        for p in self.packages_obs.union(self.packages_obs_venv):
            if p not in self.packages_devel:
                continue
            diff_new = self.project_obs
            if p not in self.packages_obs and p in self.packages_obs_venv:
                diff_new = self.project_obs_venv
            diff_old = self.project_devel
            if p in self.packages_devel_staging:
                diff_old = self.project_devel_staging
            process = self.rdiff(diff_old, p, diff_new)
            if (
                process.exit_code == 0
                and process.stdout == b""
                and process.stderr == b""
            ):
                continue
            diff.add(p)
            self.logproc(process, "rdiff on {}".format(p))
            process = self.iosc.request.list("-s", "new,review", diff_old, p)
            self.logproc(process, "requests for {0}/{1}".format(diff_old, p))
        if diff:
            logging.warning(
                "Packages in %s or %s had differences to %s or %s : %s",
                self.project_obs,
                self.project_obs_venv,
                self.project_devel_staging,
                self.project_devel,
                diff,
            )
            return False
        return True

    def print_project_diff(
        self, diff, expected_items_name, project1, project2
    ):
        result = True
        expected_items = self.expected[self.version][expected_items_name]
        unexpected_diff = diff - expected_items
        expected_diff = expected_items.intersection(diff)
        unexpected_items = expected_items - diff
        if expected_diff:
            logging.info(
                "Expected (matches expected set %s): Packages exist"
                " in %s, but not in %s : %s",
                expected_items_name,
                project1,
                project2,
                expected_diff,
            )
        if unexpected_diff:
            logging.error(
                "Unexpected (is not in expected set %s): Packages exist"
                " in %s, but not in %s : %s",
                expected_items_name,
                project1,
                project2,
                unexpected_diff,
            )
            result = False
        if unexpected_items:
            logging.error(
                "Unexpected (is in expected set %s): Packages exist in"
                " %s, and in %s : %s",
                expected_items_name,
                project1,
                project2,
                unexpected_items,
            )
            result = False
        return result

